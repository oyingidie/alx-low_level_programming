#include <stdio.h>

/**
 * add - Display the sum of two integers
 * @x: First integer
 * @y: Second integer
 *
 * Return: Result of the operation
 */
int add(int x, int y)
{
	return (x + y);
}

/**
 * sub - Display the difference of two integers
 * @x: First integer
 * @y: Second integer
 *
 * Return: Result of the operation
 */
int sub(int x, int y)
{
	return (x - y);
}

/**
 * mul - Display the product of two integers
 * @x: First integer
 * @y: Second integer
 *
 * Return: Result of the operation
 */
int mul(int x, int y)
{
	return (x * y);
}

/**
 * div - Display the quotient of two integers
 * @x: First integer
 * @y: Second integer
 *
 * Return: Result of the operation
 */
int div(int x, int y)
{
	return (x / y);
}

/**
 * mod - Display the remainder of two integers
 * @x: First integer
 * @y: Second integer
 *
 * Return: Result of the operation
 */
int mod(int x, int y)
{
	return (x % y);
}
